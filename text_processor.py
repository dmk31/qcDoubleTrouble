# text_processor.py

import re

def clean_text(text: str) -> str:
    """
    Выполняет полную предобработку текста, включая:
    1. Приведение к нижнему регистру.
    2. Удаление URL-адресов.
    3. Удаление HTML-тегов.
    4. Удаление всех символов, не являющихся русскими или латинскими буквами, цифрами или пробелами.
    5. Удаление лишних пробелов.
    
    :param text: Исходная строка текста.
    :return: Очищенная строка текста.
    """
    if not isinstance(text, str):
        return ""

    # 1. Приведение к нижнему регистру
    text = text.lower()

    # 2. Удаление URL-адресов
    text = re.sub(r'https?://\S+|www\.\S+', ' ', text)

    # 3. Удаление HTML-тегов
    text = re.sub(r'<.*?>', ' ', text)

    # 4. Удаление всех символов, которые не являются русскими или латинскими буквами, цифрами или пробелами
    text = re.sub(r'[^a-zа-я0-9\s]', ' ', text)

    # 5. Удаление лишних пробелов
    text = re.sub(r'\s+', ' ', text).strip()

    return text